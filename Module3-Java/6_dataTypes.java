class DataTypes {
    public static void main(String[] args) {
        int integerVar = 42; // Integer type
        double doubleVar = 3.14; // Double type
        float floatVar = 2.718f; // Float type
        char charVar = 'A'; // Character type
        boolean booleanVar = true; // Boolean type
        System.out.println("Integer: " + integerVar);
        System.out.println("Double: " + doubleVar);
        System.out.println("Float: " + floatVar);
        System.out.println("Character: " + charVar);
        System.out.println("Boolean: " + booleanVar);
    }
}
